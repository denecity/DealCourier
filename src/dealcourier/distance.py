def _coords_for_postcode(postcode: str) -> tuple[float, float] | None:
    """Resolve coordinates for a Swiss postcode.

    Strategy:
    1. Exact match in the lookup table.
    2. Nearest numeric PLZ in the same region (first digit matches).
       Swiss PLZs are geographically clustered, so the nearest code by
       numeric distance is usually in the same area.
    3. Nearest numeric PLZ overall, as a last-resort safety net.
    Returns None only if the input isn't a 4-digit PLZ.
    """
    # 1. Exact match
    coords = _POSTCODE_COORDS.get(postcode)
    if coords is not None:
        return coords

    # Only attempt fallback for well-formed 4-digit Swiss PLZs
    if not (postcode.isdigit() and len(postcode) == 4):
        return None

    target = int(postcode)
    first_digit = postcode[0]

    # 2. Nearest in same region
    best_diff: int | None = None
    best_coords: tuple[float, float] | None = None
    for pc, coord in _POSTCODE_COORDS.items():
        if len(pc) != 4 or not pc.isdigit():
            continue
        if pc[0] != first_digit:
            continue
        diff = abs(int(pc) - target)
        if best_diff is None or diff < best_diff:
            best_diff = diff
            best_coords = coord

    # 3. Global fallback (extremely rare — only if region is empty)
    if best_coords is None:
        for pc, coord in _POSTCODE_COORDS.items():
            if len(pc) != 4 or not pc.isdigit():
                continue
            diff = abs(int(pc) - target)
            if best_diff is None or diff < best_diff:
                best_diff = diff
                best_coords = coord

    if best_coords is not None:
        logger.debug(
            f"PLZ {postcode} not in table; using nearest known PLZ "
            f"(numeric diff={best_diff})"
        )

    return best_coords


def distance_from_zurich(postcode: str | None) -> float | None:
    """Compute distance in km from Zurich center for a Swiss postcode.

    Falls back to the nearest known PLZ in the same region if the exact
    code isn't in the lookup table. Returns None only if the input isn't
    a recognizable Swiss 4-digit postcode.
    """
    if not postcode:
        return None
    postcode = str(postcode).strip()
    coords = _coords_for_postcode(postcode)
    if coords is None:
        return None
    return round(haversine_km(ZURICH_LAT, ZURICH_LON, coords[0], coords[1]), 1)
